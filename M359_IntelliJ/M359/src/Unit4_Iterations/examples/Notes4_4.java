package Unit4_Iterations.examples;

public class Notes4_4 {
    public static void main(String[] args) {
        for (int o = 0; o < 5; o++) {
            for (int i = 0; i < 10; i++) {
                System.out.print(i + "");
            }
        }
    }
    public static void outputBreak(){
        System.out.println();
        System.out.println();
    }
}
